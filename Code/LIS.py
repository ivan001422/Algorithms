'''
АЛГОРИТМ ПОИСКА НАИБОЛЬШЕЙ ВОЗРАСТАЮЩЕЙ ПОДПОСЛЕДОВАТЕЛЬНОСТИ (LIS)

Суть: найти длину самой длинной строго возрастающей последовательности,
которую можно получить из массива, вычеркивая элементы (не меняя порядок).

Пример: [10, 9, 2, 5, 3, 7, 101, 18] → ответ 4 ([2,5,7,101] или [2,3,7,101])

Сложность: O(n²) времени, O(n) памяти
'''

m = [0, 1, 0, 3, 2, 3]
dp = [1] * len(m)  # dp[i] = длина LIS, заканчивающейся на элементе i

'''
Принцип работы:
Для каждого элемента i смотрим все предыдущие j.
Если m[j] < m[i], то можем продлить последовательность от j до i.
Длина новой = dp[j] + 1. Берем максимум из всех вариантов.
'''

for i in range(len(m)):
    for j in range(i):
        if m[j] < m[i]:
            dp[i] = max(dp[i], dp[j] + 1)

print(max(dp))  # 4
