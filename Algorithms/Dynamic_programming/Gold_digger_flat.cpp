#include <iostream>
#include <vector>

using namespace std;

// Вывод одномерного массива
void print(int *vec, int size) {
    for (int i = 0; i < size; ++i)
        cout << vec[i] << " ";
    cout << "|" << endl;
}

// Вспомогательная функция: максимум из двух чисел
int max(int a, int b) {
    if (a >= b) return a;
    return b;
}

int main() {
    // Гора (треугольник значений)
    vector<vector<int>> hill = {
        { 0 },                              // <-- Позиция кладоискателя (вершина)
        { 5, 8 },
        { 9, 8, 2 },
        { 1, 3, 5, 6 },
        { 6, 2, 4, 4, 5 },
        { 9, 5, 3, 5, 5, 7 },
        { 7, 4, 6, 4, 7, 6, 8 }
    };
    const int N = 7;           // Количество уровней

    // dp[j] — максимальная сумма, с которой можно прийти в позицию j текущего уровня
    int dp[N];
    dp[0] = hill[0][0];                      // Базовый случай: вершина

    // Обрабатываем уровни с 1 до N-1
    for (int i = 1; i < N; ++i) {
        // 1. Правый край: пришли только из правого края предыдущего уровня
        dp[i] = dp[i - 1] + hill[i][i];

        // 2. Внутренние элементы: обновляем справа налево,
        //    чтобы использовать ещё не затёртые значения dp[j] и dp[j-1] с предыдущего уровня
        for (int j = i - 1; j >= 1; --j) {
            dp[j] = max(dp[j], dp[j - 1]) + hill[i][j];
        }

        // 3. Левый край: пришли только из левого края предыдущего уровня
        //    Обновляем последним, потому что его старое значение уже использовано в шаге 2
        dp[0] = dp[0] + hill[i][0];
    }

    // Вывод итоговой строки DP (максимальные суммы для каждой позиции последнего уровня)
    print(dp, N);

    // Ответ — максимум в последней строке
    cout << "    Answer: " << *max_element(dp, dp + N) << endl;
    return 0;
}
