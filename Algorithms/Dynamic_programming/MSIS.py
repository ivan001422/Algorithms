'''
АЛГОРИТМ ПОИСКА МАКСИМАЛЬНОЙ СУММЫ ВОЗРАСТАЮЩЕЙ ПОДПОСЛЕДОВАТЕЛЬНОСТИ (MSIS)

Суть: найти максимальную сумму строго возрастающей подпоследовательности.
В отличие от LIS (который ищет длину), здесь мы суммируем элементы.

Пример: [10, 9, 2, 5, 3, 7, 101, 18] → ответ 115 ([2,5,7,101])

Сложность: O(n²) времени, O(n) памяти
'''

m = [10, 9, 2, 5, 3, 7, 101, 18]

arr = [0] * len(m)
arr[0] = m[0]  # Первый элемент сам по себе

for i in range(1, len(m)):
    # Собираем индексы элементов, меньших текущего
    l = []
    for j in range(i):
        if m[j] < m[i]:
            l.append(j)

    if len(l) == 0:
        # Нет подходящих предыдущих — только сам элемент
        arr[i] = m[i]
    else:
        # Берем лучшую сумму из предыдущих и добавляем текущий элемент
        arr[i] = m[i] + max([arr[s] for s in l])

print(arr)
print(max(arr))

