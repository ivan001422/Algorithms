'''
У нас есть 3 операции для изменения строк:
1) Удаление символа
2) Замена символа
3) Вставка символа

Пусть даны 2 строки: horse ; ros
При использовании операции вставки получим:
horse ; h+ros => можно откинуть одинаковые префиксы =>
=> Получим: orse ; ros => вставка в первую строку удалит
первый символ из второй строки

При использовании операции удаления получим:
orse ; ros => просто удаляем символ

При использовании операции замены получим:
horse ; ros -> horse ; hos -> можно откинуть первые символы
из обоих строк
'''


def is_same(s1, s2):
    return s1 != s2


s1 = 'hello'
s2 = 'hel'

l_s1 = len(s1)
l_s2 = len(s2)

matrix = [[0] * (l_s1 + 1) for _ in range(l_s2 + 1)] # Размеры матрицы: (l_s1+1) x (l_s2+1)

for i in range(1, l_s2 + 1): # Заполняем матрицу базовыми числами
    matrix[i][0] = i

for i in range(1, l_s1 + 1): # Заполняем матрицу базовыми числами
    matrix[0][i] = i

for i in range(1, l_s2 + 1):
    for j in range(1, l_s1 + 1):
        cost = 0 if s2[i - 1] == s1[j - 1] else 1
        matrix[i][j] = min(
            matrix[i][j - 1] + 1,  # Вставка // Если не хотим учитывать повторяющиеся буквы, то ВСЕГДА добавляем cost
            matrix[i - 1][j] + 1,  # Удаление // Если не хотим учитывать повторяющиеся буквы, то ВСЕГДА добавляем cost
            matrix[i - 1][j - 1] + cost  # Замена/пропуск
        )
print(matrix)

# Красивый вывод
print("  ", " ".join(s1))
for i in range(l_s2 + 1):
    row = [s2[i - 1] if i > 0 else " "]
    row.extend(str(matrix[i][j]) for j in range(l_s1 + 1))
    print(" ".join(row))

'''
Почему это работает?
Данный алгоритм называется алгоритмом Вагнера-Фишера
и является улучшенной версией рекуррентной формы
расчета расстояния Левенштейна.

СВЯЗЬ МЕЖДУ ОПЕРАЦИЯМИ И ИНДЕКСАМИ МАТРИЦЫ:

1) ВСТАВКА (matrix[i][j-1] + 1):
   Смотрим на значение СЛЕВА от текущей клетки.
   Это соответствует решению для строк s2[0:i] и s1[0:j-1],
   к которому мы добавляем ОДИН символ из s1 в s2.

   Из пояснения в начале: когда мы делаем вставку в начало,
   мы как бы "удаляем" первый символ из второй строки,
   поэтому переходим к j-1 (уменьшаем целевую строку).

2) УДАЛЕНИЕ (matrix[i-1][j] + 1):
   Смотрим на значение СВЕРХУ от текущей клетки.
   Это соответствует решению для строк s2[0:i-1] и s1[0:j],
   к которому мы УДАЛЯЕМ один символ из s2.

   Из пояснения: при удалении мы просто убираем символ из первой строки,
   поэтому переходим к i-1 (уменьшаем исходную строку).

3) ЗАМЕНА (matrix[i-1][j-1] + cost):
   Смотрим на значение ПО ДИАГОНАЛИ (сверху-слева).
   Это соответствует решению для строк s2[0:i-1] и s1[0:j-1],
   к которому мы либо ЗАМЕНЯЕМ символ (если они разные, cost=1),
   либо НИЧЕГО НЕ ДЕЛАЕМ (если символы совпадают, cost=0).

   Из пояснения: при замене мы можем откинуть первые символы
   обеих строк, поэтому переходим к i-1 и j-1 одновременно.

Таким образом, каждая клетка matrix[i][j] хранит минимальное
количество операций для превращения ПЕРВЫХ i символов s2
в ПЕРВЫЕ j символов s1, опираясь на уже вычисленные решения
для меньших подстрок.
'''
