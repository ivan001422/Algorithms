#include <iostream>
#include <cstdlib>   // для max()
using namespace std;

/*
Дан массив чисел (могут быть отрицательные).
Найти непрерывную подпоследовательность (подмассив), сумма элементов которой максимальна.
Пример: для массива {-1, 2, 4, -3, 5, 2, -5, 2} максимальная сумма = 10 (подмассив {2, 4, -3, 5, 2}).
*/

int main() {
    // Исходный массив
    int arr[]{ -1, 2, 4, -3, 5, 2, -5, 2 };

    // 3 << 30 = 3 * 2^30 = 3'221'225'472, но в знаковом int это 
    // превращается в отрицательное число -1'073'741'824 (из-за старшего бита)
    // Используется как "минус бесконечность" для корректной инициализации
    int best, sum;
    best = sum = 3 << 30;  // -1073741824 (очень маленькое число)

    for (int i = 0; i != 8; ++i) {
        // Текущий элемент либо начинаем новый подмассив, либо добавляем к существующему
        // sum = max( arr[i], arr[i] + sum ) — ядро алгоритма Кадане
        sum = max(arr[i], arr[i] + sum);

        // Обновляем глобальный максимум
        best = max(sum, best);
    }

    cout << "Max: " << best;  // Выведет 10
}
