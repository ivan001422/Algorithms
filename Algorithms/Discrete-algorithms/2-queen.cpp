/**
 * ============================================================================
 * Задача: количество способов расставить двух неатакующих ферзей на доске n x n
 * ============================================================================
 *
 * Решение за O(n) с использованием рекуррентного соотношения.
 *
 * Идея:
 *   Рассмотрим последнюю строку и последний столбец доски n x n.
 *   Пусть q(n) — искомое число расстановок.
 *
 *   1. Если на последней строке и последнем столбце нет ферзей,
 *      то они оба находятся на доске (n-1)x(n-1) →  q(n-1) способов.
 *
 *   2. Если хотя бы один ферзь стоит на последней строке или столбце:
 *      - Множество таких клеток имеет размер 2n-1.
 *      - Ферзь на такой клетке бьёт 3(n-1) клеток, значит для второго ферзя
 *        остаётся n^2 - 3(n-1) - 1 свободных позиций.
 *      - Произведение (2n-1) * (n^2 - 3(n-1) - 1) даёт число расстановок
 *        с учётом случаев, когда оба ферзя находятся на границе.
 *      - Расстановок, где оба ферзя лежат на последней строке/столбце,
 *        ровно (n-1)(n-2) — их мы посчитали дважды, поэтому вычитаем.
 *
 *   После упрощения получаем рекуррентную формулу:
 *        q(n) = q(n-1) + 2·(n-1)²·(n-2),   q(1)=0, q(2)=0.
 *
 *   Таким образом, последовательно вычисляем q(i) для i = 3..n за O(n).
 * ============================================================================
 */

#include <iostream>
using namespace std;

long long count_two_queens_linear(int n) {
    if (n < 2) return 0;          // для n = 0 или 1 ответ 0

    long long q = 0;              // q(1) = 0
    for (int i = 2; i <= n; ++i) {
        // q(i) = q(i-1) + 2*(i-1)^2*(i-2)
        q += 2LL * (i - 1) * (i - 1) * (i - 2);
    }
    return q;
}

int main() {
    int n;
    cout << "Введите размер доски n: ";
    cin >> n;

    long long result = count_two_queens_linear(n);
    cout << "Количество способов: " << result << endl;

    return 0;
}

