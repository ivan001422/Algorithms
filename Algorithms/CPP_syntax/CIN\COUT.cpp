#include <iostream>
#include <string>

using namespace std;

/*
 * ============================================================
 *  Ввод и вывод в C++: cin, cout, getline
 * ============================================================
 *
 * 1. РАЗДЕЛИТЕЛИ ПРИ ВВОДЕ
 *    cin использует пробелы, табуляции и символы новой строки
 *    как разделители. Можно передавать несколько значений подряд,
 *    если они отделены любыми пробельными символами.
 *
 * 2. ЧТЕНИЕ СТРОК С ПРОБЕЛАМИ
 *    Оператор >> читает строку до первого пробельного символа.
 *    Чтобы прочитать строку целиком (включая пробелы), используйте
 *    getline. Однако после форматированного ввода (>>) в потоке
 *    остаётся символ '\n', поэтому прямой вызов getline вернёт пустую
 *    строку. Перед getline нужно очистить буфер с помощью cin.ignore().
 *
 * 3. '\n' против endl
 *    '\n' просто переводит строку. endl делает то же самое,
 *    но дополнительно сбрасывает буфер вывода. Поэтому '\n' работает
 *    быстрее, и его стоит использовать, если принудительный сброс не нужен.
 *
 * 4. ЧТЕНИЕ ДО КОНЦА ВВОДА
 *    Цикл while (cin >> переменная) читает данные до тех пор,
 *    пока не встретит конец файла (EOF) или некорректное значение.
 *    Удобен, когда заранее неизвестно количество вводимых элементов.
 */

int main() {
    // ---------------------------------------------------------
    // Пример 1: ввод нескольких чисел и строки (без пробелов)
    // ---------------------------------------------------------
    int a, b;
    string word;

    cout << "Введите два числа и слово: ";
    cin >> a >> b >> word;
    cout << "a = " << a << ", b = " << b << ", word = " << word << "\n\n";

    // Варианты ввода:
    //   >> 10 20 hello     (пробелы)
    //   >> 10   20	hello   (табуляция)
    //   >> 10
    //      20
    //      hello           (переносы строк)
    // Все они дадут одинаковый результат.

    // ---------------------------------------------------------
    // Пример 2: строка с пробелами – проблема и решение
    // ---------------------------------------------------------
    int x, y;
    string sentence;

    cout << "Введите два числа и строку с пробелами: ";
    cin >> x >> y;
    cin.ignore();               // убираем оставшийся '\n' после чисел
    getline(cin, sentence);
    cout << "x = " << x << ", y = " << y
         << ", sentence = \"" << sentence << "\"\n\n";

    // Без cin.ignore() переменная sentence осталась бы пустой,
    // т.к. getline сразу прочитала бы символ новой строки.

    // ---------------------------------------------------------
    // Пример 3: сравнение '\n' и endl
    // ---------------------------------------------------------
    cout << "Строка с \\n";   // буфер может не сброситься сразу
    cout << "Строка с endl" << endl; // буфер сброшен

    // ---------------------------------------------------------
    // Пример 4: чтение до конца ввода (Ctrl+D / Ctrl+Z)
    // ---------------------------------------------------------
    int value;
    cout << "Вводите целые числа (Ctrl+D для завершения):\n";
    while (cin >> value) {
        cout << "Прочитано: " << value << '\n';
    }
    cout << "Ввод завершён.\n";

    return 0;
}
