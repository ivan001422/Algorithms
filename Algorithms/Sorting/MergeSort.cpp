#include <iostream>
#include <vector>

using namespace std;

// Прототипы функций (объявления)
void merge(vector<int> &a, int l, int r);      // Слияние двух отсортированных половин
void mergeSort(vector<int> &a, int l, int r);  // Рекурсивная сортировка слиянием

int main() {
    // Исходный массив для сортировки
    vector<int> arr = { 38, 27, 43, 3, 9, 82, 10, 5 };

    // Вывод до сортировки
    cout << "До сортировки: ";
    for (int x : arr) cout << x << " ";
    cout << endl;

    // Запуск сортировки: передаём весь массив (индексы от 0 до 7)
    mergeSort(arr, 0, 7);

    // Вывод после сортировки
    cout << "После сортировки: ";
    for (int x : arr) cout << x << " ";
    cout << endl;

    return 0;
}

// -------------------------------------------------------------
// Функция слияния двух соседних отсортированных отрезков:
//   a[l..m]   – левая половина (уже отсортирована)
//   a[m+1..r] – правая половина (уже отсортирована)
// Результат: a[l..r] становится отсортированным по возрастанию
// -------------------------------------------------------------
void merge(vector<int> &a, int l, int r) {
    // Вычисляем середину текущего отрезка (индекс последнего элемента левой части)
    int m = (l + r) / 2;

    // Размеры левой и правой половин
    int sizel = m - l + 1; // элементы от l до m включительно
    int sizer = r - m;     // элементы от m+1 до r включительно

    // Создаём временные векторы и копируем в них значения из соответствующих частей
    vector<int> left(sizel), right(sizer);

    for (int i = 0; i < sizel; ++i)
        left[i] = a[l + i];          // копируем левую половину

    for (int j = 0; j < sizer; ++j)
        right[j] = a[m + 1 + j];     // копируем правую половину

    // Индексы для прохода по временным массивам и основному
    int i = 0;    // текущий индекс в left
    int j = 0;    // текущий индекс в right
    int k = l;    // текущий индекс в основном массиве a, куда будем записывать

    // Основной цикл слияния: пока есть элементы в обоих временных массивах,
    // выбираем меньший и помещаем в a[k], сдвигая соответствующие указатели.
    while (i != sizel && j != sizer) {
        if (left[i] <= right[j]) {
            a[k++] = left[i++];
        }
        else {
            a[k++] = right[j++];
        }
    }

    // Если в левом массиве остались элементы, копируем их в конец (они уже отсортированы)
    while (i != sizel) {
        a[k++] = left[i++];
    }

    // Если в правом массиве остались элементы, копируем их в конец
    while (j != sizer) {
        a[k++] = right[j++];
    }
    // После завершения слияния отрезок a[l..r] полностью отсортирован
}

// -------------------------------------------------------------
// Рекурсивная сортировка слиянием для отрезка a[l..r]
// -------------------------------------------------------------
void mergeSort(vector<int> &a, int l, int r) {
    // Базовый случай: если отрезок содержит 0 или 1 элемент,
    // он считается уже отсортированным (выход из рекурсии)
    if (l >= r) return;

    // Вычисляем середину (без переполнения, безопасный способ)
    int m = l + (r - l) / 2;

    // Рекурсивно сортируем левую половину [l .. m]
    mergeSort(a, l, m);

    // Рекурсивно сортируем правую половину [m+1 .. r]
    mergeSort(a, m + 1, r);

    // После того как обе половины отсортированы, сливаем их в одну
    merge(a, l, r);
}
