#include <iostream>
#include <vector>

using namespace std;

// Прототип рекурсивной функции
void search(int k);

// Функция вывода содержимого вектора
void print(vector<int> a) {
    for (int i = 0; i != a.size(); ++i) {
        cout << a[i] << ' ';
    }
    cout << '\n';
}

int n = 3;              // Размер множества {1, 2, ..., n}
vector<int> subset;     // Текущее подмножество (строится рекурсивно)

int main() {
    search(1);          // Начинаем перебор с числа 1
    return 0;
}

// Рекурсивная генерация всех подмножеств арифметической прогрессии
// k — текущее рассматриваемое число (от 1 до n)
void search(int k) {
    // Базовый случай: все числа от 1 до n обработаны
    if (k == n + 1) {
        print(subset);  // Выводим текущее подмножество
        return;
    }

    // 1) Включаем число k в подмножество
    subset.push_back(k);    // Добавляем k
    search(k + 1);          // Рекурсивно перебираем следующие числа
    subset.pop_back();      // Убираем k (возврат, backtracking)

    // 2) Не включаем число k в подмножество
    search(k + 1);          // Сразу переходим к следующему числу
}
