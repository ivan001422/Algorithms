print("2. f-СТРОКИ (FORMATTED STRINGS)")
print("=" * 60)

"""
f-строки - это способ форматирования строк в Python,
который позволяет встраивать выражения Python
непосредственно в строки

Базовый синтаксис:
f'текст {ПЕРЕМЕННАЯ} текст'
"""

# --- Подстановка значений ---
print("\n--- Подстановка значений переменных ---")

age = 10
k = 5
name = 'Ivan'

# Обычная подстановка
print(f'Hi, i\'m {name}, i\'m {age} years old')  # \' экранирует кавычку

# Выражения внутри {}
print(f"Сумма: {age + k}")                    # 15
print(f"Квадрат: {k ** 2}")                   # 25
print(f"Имя в верхнем регистре: {name.upper()}")  # IVAN

# --- Форматирование чисел ---
print("\n--- Форматирование чисел ---")

pi = 3.1415926535
num = 42
percent = 0.9567

# Дроби
print(f"Два знака: {pi:.2f}")              # 3.14
print(f"Пять знаков: {pi:.5f}")            # 3.14159

# Проценты
print(f"Точность: {percent:.2%}")          # 95.67%
print(f"Точность: {percent:.1%}")          # 95.7%

# Целые числа
print(f"Число: {num:5d}")                  # "   42" (ширина 5)
print(f"Число: {num:05d}")                 # "00042" (дополнение нулями)

# Экспоненциальная форма
print(f"Наука: {pi:.2e}")                  # 3.14e+00

# --- Системы счисления ---
print("\n--- Системы счисления ---")

num = 42

print(f"Десятичная: {num}")                # 42
print(f"Десятичная (явно): {num:d}")       # 42
print(f"Двоичная: {num:b}")                # 101010
print(f"Восьмеричная: {num:o}")            # 52


# --- Выравнивание ---
print("\n--- Выравнивание ---")

text = "Python"

print(f"|{text:<10}|")      # Левое (по умолчанию)   |Python    |
print(f"|{text:>10}|")      # Правое                 |    Python|
print(f"|{text:^10}|")      # Центрирование          |  Python  |
print(f"|{text:*^10}|")     # С заполнителем         |**Python**|

# --- Многострочные f-строки ---
print("\n--- Многострочные f-строки ---")

k = 5
acc = 0.95

# тройные кавычки
print(f"""
Результаты KNN:
  k = {k}
  Точность = {acc:.2%}
""")

# --- Экранирование фигурных скобок ---
print("\n--- Экранирование фигурных скобок ---")

# print(f'Скобки: {}')  # Ошибка! Пустые скобки недопустимы
print(f'Скобки: {{}}')        # Выведет: Скобки: {}
print(f'{{ {name} }}')        # Выведет: { Ivan }


