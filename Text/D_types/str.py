print("3. МЕТОД .format()")
print("=" * 60)

"""
Метод .format() - это инструмент для форматирования строк.
Позволяет вставлять значения переменных, выражений или 
результаты выполнения функций внутрь строки

Базовый синтаксис:
"шаблон".format(аргументы)
"""

# --- Позиционные аргументы ---
print("\n--- Позиционные аргументы ---")

# По порядку
print("{} + {} = {}".format(2, 3, 5))           # 2 + 3 = 5

# С индексами
print("{0} + {1} = {2}".format(2, 3, 5))        # 2 + 3 = 5
print("{1} + {0} = {2}".format(2, 3, 5))        # 3 + 2 = 5

# Динамические вычисления с аргументами метода
a = b = 5
print('{0} + {1} = {sum}'.format(a, b, sum=a+b)) # sum нужно указывать как именованный агрумент

# Повторное использование
print("{0} {0} {1}".format("hello", "world"))   # hello hello world

# --- Именованные аргументы ---
print("\n--- Именованные аргументы ---")

print("{model}, k={k}, acc={acc:.2%}".format(
    model="KNN",
    k=5,
    acc=0.9567
))
# KNN, k=5, acc=95.67%

# --- Распаковка словаря ---
print("\n--- Распаковка словаря ---")

data = {'model': 'KNN', 'k': 5, 'acc': 0.9567}
print("{model}, k={k}, acc={acc:.2%}".format(**data))
# KNN, k=5, acc=95.67%

# --- Распаковка списка ---
print("\n--- Распаковка списка ---")

values = [2, 3, 5]
print("{} + {} = {}".format(*values))          # 2 + 3 = 5

# --- Форматирование чисел в .format() ---
print("\n--- Форматирование чисел в .format() ---")

pi = 3.14159
print("{:.2f}".format(pi))      # 3.14
print("{:.2%}".format(0.9567))  # 95.67%
print("{:05d}".format(42))      # 00042

print('{:.2} {:b}'.format(0.973, 64)) # Аргументы подставятся перед двоеточием внутри фигурных скобок

# --- Выравнивание в .format() ---
print("\n--- Выравнивание в .format() ---")

text = "Python"
print("|{:<10}|".format(text))   # |Python    | 
print("|{:>10}|".format(text))   # |    Python|
print("|{:^10}|".format(text))   # |  Python  |
print("|{:*^10}|".format(text))  # |**Python**|
